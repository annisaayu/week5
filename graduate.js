function graduates (students) {
  // Code disini
  let result={};
  if(students.length===0){
    return result
  }

  for(let i=0; i<students.length; i++){
    let list=[];
    let kelas=students[i].class;
    result[kelas] = list;

    for(let j=0; j<students.length; j++){
      if(students[j].score > 75 && students[i].class === students[j].class){
        let obj={};
        obj.name = students[j].name;
        obj.score = students[j].score;
        list.push(obj);
      }
    }

  }
  return result;
}

